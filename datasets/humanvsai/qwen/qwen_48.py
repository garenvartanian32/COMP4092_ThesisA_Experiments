def write_jobfile(self, task, **kwargs):
    jobfile_path = os.path.join(self.jobdir, f'{task.name}.sh')
    with open(jobfile_path, 'w') as jobfile:
        jobfile.write('#!/bin/bash\n')
        jobfile.write(f'#SBATCH --job-name={task.name}\n')
        jobfile.write(f'#SBATCH --output={task.name}.out\n')
        jobfile.write(f'#SBATCH --error={task.name}.err\n')
        jobfile.write(f'#SBATCH --time={task.time}\n')
        jobfile.write(f'#SBATCH --nodes={task.nodes}\n')
        jobfile.write(f'#SBATCH --ntasks-per-node={task.ntasks_per_node}\n')
        jobfile.write(f'#SBATCH --cpus-per-task={task.cpus_per_task}\n')
        jobfile.write(f'#SBATCH --mem={task.mem}\n')
        jobfile.write(f'#SBATCH --partition={task.partition}\n')
        jobfile.write(f'#SBATCH --gres={task.gres}\n')
        jobfile.write(f'#SBATCH --mail-type={task.mail_type}\n')
        jobfile.write(f'#SBATCH --mail-user={task.mail_user}\n')
        jobfile.write(f'#SBATCH --constraint={task.constraint}\n')
        jobfile.write(f'#SBATCH --account={task.account}\n')
        jobfile.write(f'#SBATCH --qos={task.qos}\n')
        jobfile.write(f'#SBATCH --reservation={task.reservation}\n')
        jobfile.write(f'#SBATCH --exclusive={task.exclusive}\n')
        jobfile.write(f'#SBATCH --requeue={task.requeue}\n')
        jobfile.write(f'#SBATCH --nodelist={task.nodelist}\n')
        jobfile.write(f'#SBATCH --exclude={task.exclude}\n')
        jobfile.write(f'#SBATCH --dependency={task.dependency}\n')
        jobfile.write(f'#SBATCH --array={task.array}\n')
        jobfile.write(f'#SBATCH --jobid={task.jobid}\n')