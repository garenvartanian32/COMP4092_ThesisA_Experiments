def get_config(self):
    config = configparser.ConfigParser()
    config.read('config.ini')
    self.config = config['DEFAULT']
    self.config['api_key'] = self.config.get('api_key', 'default_api_key')
    self.config['api_secret'] = self.config.get('api_secret', 'default_api_secret')
    self.config['base_url'] = self.config.get('base_url', 'https://api.example.com')
    self.config['timeout'] = self.config.getint('timeout', 10)
    self.config['retry_attempts'] = self.config.getint('retry_attempts', 3)
    self.config['log_level'] = self.config.get('log_level', 'INFO')
    self.config['enable_logging'] = self.config.getboolean('enable_logging', True)
    self.config['use_ssl'] = self.config.getboolean('use_ssl', True)
    self.config['verify_ssl'] = self.config.getboolean('verify_ssl', True)
    self.config['proxy'] = self.config.get('proxy', None)
    self.config['user_agent'] = self.config.get('user_agent', 'MyApp/1.0')
    self.config['cache_enabled'] = self.config.getboolean('cache_enabled', False)
    self.config['cache_ttl'] = self.config.getint('cache_ttl', 300)
    self.config['cache_max_size'] = self.config.getint('cache_max_size', 1000)
    self.config['rate_limit'] = self.config.getint('rate_limit', 100)
    self.config['rate_limit_period'] = self.config.getint('rate_limit_period', 60)
    self.config['rate_limit_burst'] = self.config.getint('rate_limit_burst', 20)
    self.config['rate_limit_retry_delay'] = self.config.getint('rate_limit_retry_delay', 5)
    self.config['rate_limit_retry_attempts'] = self.config.getint('rate_limit_retry_attempts', 3)
    self.config['rate_limit_backoff_factor'] = self.config.getfloat('rate_limit_backoff_factor', 1.0)
    self.config['rate_limit_jitter'] = self.config.getfloat('rate_limit_jitter', 0.1)